// ─── INIT ─────────────────────────────────────────────────────────────────────
(function() {
  var lang = 'en';
  try {
    var url = new URL(window.location.href);
    var urlLang = url.searchParams.get('lang');
    if (urlLang && window.LANG[urlLang]) { lang = urlLang; }
    else {
      var saved = localStorage.getItem('fw_lang');
      if (saved && window.LANG[saved]) lang = saved;
    }
  } catch(e) {}
  if (lang !== 'en') {
    if (document.readyState === 'loading') {
      document.addEventListener('DOMContentLoaded', function() { window.setLang(lang); });
    } else {
      window.setLang(lang);
    }
  }
})();
